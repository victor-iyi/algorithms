#include <stdio.h>

// NOTE: All functions must prepend C_ in their signature.

/*
 * +—————————————————————————————————————————————————————————————————————————————————————+
 * | +—————————————————————————————————————————————————————————————————————————————————+ |
 * | | Find the nth fibonacci number.
 * | +—————————————————————————————————————————————————————————————————————————————————+ |
 * +—————————————————————————————————————————————————————————————————————————————————————+
 */
int C_fibonacci(int n)
{
  // Local variables.
  int a = 0, b = 1, temp;
  for (int i = 0; i < n; i++)
  {
    a = temp;
    a = a + b;
    temp = b;
  }

  return a;
}

/*
 * +—————————————————————————————————————————————————————————————————————————————————————+
 * | +—————————————————————————————————————————————————————————————————————————————————+ |
 * | | Find the factorial of a given number.
 * | +—————————————————————————————————————————————————————————————————————————————————+ |
 * +—————————————————————————————————————————————————————————————————————————————————————+
 */
int C_factorial(int n)
{

  // Base case.
  if (n < 2)
    return n;

  // Recursive call.
  return C_factorial(n - 1);
}
